# Entrada edad de Juan
Edad_Juan = int(input("Ingrese la edad de Juan: "))

# Proceso
Edad_Aberto = int((2/3) * Edad_Juan)
Edad_Ana = int((4/3) * Edad_Juan)
Edad_Mama= (Edad_Aberto + Edad_Juan + Edad_Ana)

# Edades
print(f"Edad de Alberto: {Edad_Aberto} años")
print(f"Edad de Juan: {Edad_Juan} años")
print(f"Edad de Ana: {Edad_Ana} años")
print(f"Edad de la mamá: {Edad_Mama} años")
